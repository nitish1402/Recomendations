def sim_distance(list,person1,person2):

    #defining a list to see if they have anything in common
    si={}

    for item in list[person1]:
        if item in list[person2]:
            si[item]=1

    #returning 0 if no similarity found
    if len(si)==0: return 0

    #calculating sum of squares in matching terms
    #print si

    sum_of_squares=sum([pow(list[person1][item]-list[person2][item],2)
                      for item in list[person1] if item in list[person2]])

    #returning the calculated value by inverting

    return 1/(1+sum_of_squares)





